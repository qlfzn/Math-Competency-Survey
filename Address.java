public class Address {
    String street;
    String postcode;
    String city;
    String state;


    void setStreet(String s){
        this.street = s;
    }

    void setPostcode(String p){
        this.postcode = p;
    }

    void setCity(String c){
        this.city = c;
    }

    void setState(String st){
        this.state = st;
    }

}
